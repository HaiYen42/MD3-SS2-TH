import com.sun.org.apache.xpath.internal.objects.XString;
import com.sun.org.apache.xpath.internal.objects.XStringForChars;

public class KhaiBaoSuDungBien {
    public static void main(String[] args) {
        int i= 10; // khai báo biến i là kiểu int có giá trị là 10;
        float f= 20.5f; // khai báo biến f có kiểu là float có giá trị là 20.5
        boolean b = true; // khai báo biến b có kiểu boolean có giá trị là true;
        char c = 'a'; // Khai báo biến c có kiểu char có giá trị là ký tự a
        String s = "Hà Nội"; // Khai báo biến s có kiểu là string
        System.out.println("i= " + i);
        System.out.println("f= "+ f);
        System.out.println("b= "+ b);
        System.out.println("c= "+ c);
        System.out.println("s=  "+s);

    }
}
